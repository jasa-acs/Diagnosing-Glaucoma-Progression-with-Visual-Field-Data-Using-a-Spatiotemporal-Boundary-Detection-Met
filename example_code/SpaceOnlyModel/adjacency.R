###Function to create W from adjacency
adj.mat<-function(Adj) {
	n<-length(Adj)
	W<-matrix(0,nrow=n,ncol=n)
	for (i in 1:n) {
		W[i,Adj[[i]]]<-rep(1,length(Adj[[i]]))
	}	
	return(W)
}

